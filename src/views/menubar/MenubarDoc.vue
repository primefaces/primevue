&lt;Menubar :model="items"&gt;
    &lt;template #start&gt;
        Before
    &lt;/template&gt;
    &lt;template #end&gt;
        After
    &lt;/template&gt;
&lt;/Menubar&gt;
</template>
</CodeHighlight>

